<p>Mettre en page un article se fait à l'aide de quelques codes très simple :</p>
<br>
<p>
	<b>## Titre</b><br>
	<b>*italique*</b><br>
	<b>**gras**</b><br>
</p>
<br>
<p>D'autres sont un tout petit peu plus élaborés :</p>
<br>
<p>
	<b>Transformer un paragraphe en chapo de page</b><br>
	(chapo...)<br>
	Texte du chapo <br>
	(...chapo)
</p>
<br>
<p>
	<b>Ouvrir un lien dans une nouvelles fenêtre :</b><br>
	Ajouter "popup: yes" à la fin du lien<br>
	(link: http://... text: ... popup: yes)<br>
</p>
<br>
<p>
	<b>Faire des colonnes :</b><br>
	(columns...)<br>
	Texte de la première colonne<br>
	++++<br>
	Texte de la seconde colonne<br>
	(...columns)
</p>
<br>
<p>
	<b>Insérer une vidéo :</b><br>
	(embed: http://vimeo.com/3432886)<br>
</p>
<br>
<p>
	<b>Insérer une forme :</b><br>
	(forme: ligne)<br>
	ou cercle / demi-cercle / ellipse / carre / losange
</p>
